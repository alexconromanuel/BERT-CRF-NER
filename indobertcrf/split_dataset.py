import json
import codecs
import random
import math

with open('singgalang.json') as f:
    list_singgalang = json.load(f)

# demo
# list_singgalang = list_singgalang[:500]
# al 
random.shuffle(list_singgalang)
threshold = math.ceil(len(list_singgalang) * 0.7)

def save_file(list_input, str_fparth):
    with open(str_fparth, "wb") as f:
        json.dump(list_input,
                  codecs.getwriter("utf-8")(f),
                  ensure_ascii=False,
                  indent = 4)

save_file(list_singgalang[:threshold], "singgalang-total-train.json")
save_file(list_singgalang[threshold:], "singgalang-total-dev.json")